                                                <p class="m-0 font-weight-bold text-primary">1. Ordonnance-Loi n°206 du 29 juin 1964 portant création de l’INPP</p><br>
                                                <p align="justify">L’article 8 relatif aux finances énumère les différentes ressources de l’INPP, entre autres, la cotisation des employeurs proportionnelles à la somme des salaires versés par eux à leur personnel.</p> <br>
                                                <p align="justify">Le taux de la cotisation est fixé, pour chaque période de trois ans par arrêté conjoint du Ministre de Finance et du Travail après avis conforme des organisations syndicales les plus représentatives et les organisations
                                                    d’employeurs.</p> <br>
                                                <p class="m-0 font-weight-bold text-primary">2. Ordonnance-Loi n°78-199 du 5 mai 1978 portant Statuts d’une entreprise publique dénommée INPP</p><br>
                                                <p align="justify">en son article 4 relatif aux ressources de l’INPP reprend le dispositif de l’article 8 de l’Ordonnance-Loi n°206 du 29 juin 1964 ci-haut cité.</p> <br>
                                                <p class="m-0 font-weight-bold text-primary">3. Ordonnance-Loi n°015/2002 du 16 octobre 2002 portant Code du Travail, notamment les articles 1, 8, 11, 15 et 16</p><br>
                                                <p align="justify">L’article 1 de la loi sus-indiquée précise la sphère de son plan d’application qui s’étend à tous les travailleurs et à tous les employeurs exerçant leurs activités en RDC et exclut de ce fait les magistrats, les agents de carrière des services publics de l’Etat régis par le statut général, les agents et fonctionnaires de carrière des services publics de l’Etat régis par les statuts particuliers ainsi que les éléments des forces armées congolaises, de la Police Nationale Congolaise et du Service National.</p><br>
                                                <p align="justify">L’article 8 astreint tout employeur public ou privé à procéder à la formation, au perfectionnement et à l’adaptation professionnelle des travailleurs qu’il emploie et pour cela, ils doivent recourir à l’INPP.</p><br>
                                                <p align="justify">Les articles 11, 12 13 et 14 traitent de la création de l’INPP, son statut juridique, ses missions et de la tutelle que l’État exerce sur lui. L’article
                                                    15 relatif aux ressources reprend le dispositif de l’Ordonnance-Loi n°206 avec la seule différence qu’il est précisé que la cotisation patronale est payée mensuellement et proportionnellement à la somme des rémunérations versées par les employeurs à leur personnel au cours du trimestre précédent.
                                                </p><br>
                                                <p align="justify">L’article 16 relève que les sommes dues à l’INPP certifiées par le Ministre du Travail vaut titre permettant les saisies suivant la procédure prévue par les articles 106 et suivants du Code de procédure civile.</p>
                                                <p class="m-0 font-weight-bold text-primary">4. Ordonnance n°84-186 du 15 octobre </p><br>
                                                <p align="justify">Fixe
                                                    les modalités de paiement de la cotisation due par les employeurs à l’INPP ainsi que ses mesures d’applications. Cette ordonnance spécifie que tout employeur est tenu de payer sa cotisation à l’INPP et ce, conformément à l’article 15 du Code du Travail, mais un dégrèvement est accordé par le Ministère du Travail aux employeurs qui assurent eux-mêmes la formation de leurs travailleurs, ce après avis de leur délégation syndicale ainsi que l’avis technique de l’INPP.
                                                </p>
                                                <p class="m-0 font-weight-bold text-primary">Déclaration des cotisations dues à l’INPP</p><br>
                                                <p align="justify">Le montant global des rémunérations à déclarer à l’INPP devrait être au moins égal au montant des sommes déclarées au service des impôts au titre des rémunérations payées aux travailleurs.
                                                <ul>
                                                    <li class="m-0 font-weight-bold text-success">La declaration mensuelle</li>
                                                    <p align="justify">Elle se fait sur le formulaire modèle III de l’INPP, que l’employeur remplit et retourne à l’INPP l’original et une
                                                        copie avant le 05 du mois. <br>
                                                        Les employeurs qui ont installé des agences ou des succursales dans des provinces sont tenus d’adresser leur déclaration de rémunération dite modèle III aux directions provinciales de l’INPP installées à travers la République.
                                                    </p>
                                                </ul>
                                                </p>
                                                <p class="m-0 font-weight-bold text-primary">Procédure de paiement</p><br>
                                                <p align="justify">Les employeurs sont tenus d’effectuer en priorité leurs paiements par voie bancaire auprès des banques de la place, par espèces auprès des caisses de l’INPP ou encore auprès de mandataires de l’INPP munis d’un ordre de mission.
                                                    Les cotisations sont payables dans les 10 jours qui suivent la fin du mois.
                                                </p>

